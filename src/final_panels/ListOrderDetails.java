     public  void addRW(int k) throws SQLException{
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[4];
         admin a = new admin();
        
       // ArrayList<orderDetails> orders = a.listorderdetails(5);
        ArrayList<orderDetails> orders = a.listorderdetails(k);
      
       
        for(orderDetails m : orders)
        {
            
            rowData[0] = m.getMedicineName();
            rowData[1] = m.getMedicineType();
            rowData[2] = m.getOrderQuantity();
            rowData[3] = k;
            model.addRow(rowData);
        }
        
         setDefaultCloseOperation(ListOrderDetails.DISPOSE_ON_CLOSE);
        
         
    
       
 }
     
     

